    private void llenarTabla() {
        Connection con = ConnectDB.getConnection();
        Statement consulta = null;
        if (con != null) {
            try {
                consulta = con.createStatement();
                ResultSet rs = consulta.executeQuery("Select * FROM proyecto WHERE clienteId = " + "'" + clienteModelo.getId() + "';");
                modelo.setColumnCount(7);
                modelo.setColumnIdentifiers(new Object[]{"Id", "Nombre", "Categoria", "Encargado", "Estado"});
                while (rs.next()) {
                    modelo.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(4), rs.getString(5), rs.getString(6)});
                }
            } catch (SQLException ex) {
                Logger.getLogger(ClientesAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

